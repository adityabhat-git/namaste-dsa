// Adds two values and logs the sum to the console.
function sum(a, b) { 
    let add = a + b; // compute the sum
    console.log(add); // output the sum
}

// Multiplies two values and logs the product.
function multiply(a, b) { 
    let result = a * b; // compute the product
    console.log(result); // output the result
}

// Returns the square of the input value.
function square(a) {
    let result = a * a; // multiply the value by itself
    return result; // return the squared value
}

